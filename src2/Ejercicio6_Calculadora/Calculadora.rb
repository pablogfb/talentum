class Calculadora
   
   

    def self.suma valor1,valor2
    valor1 + valor2
    end
    
    def self.resta valor1,valor2
    valor1 - valor2
    end
    
    def self.multiplica valor1,valor2
    valor1 * valor2
    end
    
    def self.divide valor1,valor2
    valor1 / valor2
    end
    

end
     
